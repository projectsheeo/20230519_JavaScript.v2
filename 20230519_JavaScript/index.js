// 集計ボタンを押したら、クラスが付いているボタンのテキストを取り出し、テキストの頭に"@"を付ける
let btn_aggregate = document.getElementById("btn_aggregate");
btn_aggregate.addEventListener("click", function () {
    let btns = document.getElementsByClassName("clicked");
    let btns_text = [];
    for (let i = 0; i < btns.length; i++) {
        btns_text.push(btns[i].textContent);
        btns_text[i] = "@" + btns_text[i] + "  ";
    }
    console.log(btns_text);

    // 集計結果をランダムな順番でコンソールに表示する
    let btns_text_random = [];
    while (btns_text.length > 0) {
        let random = Math.floor(Math.random() * btns_text.length);
        btns_text_random.push(btns_text[random]);
        btns_text.splice(random, 1);
    }
    console.log(btns_text_random);

    // 集計結果を4個ずつ塊にして表示する
    let btns_text_random_chunk = [];
    while (btns_text_random.length > 0) {
        btns_text_random_chunk.push(btns_text_random.splice(0, 4));
    }
    console.log(btns_text_random_chunk);

    //上記の結果をHTMLに表示する
    let result = document.getElementById("result");
    result.innerHTML = "";
    for (let i = 0; i < btns_text_random_chunk.length; i++) {
        result.innerHTML += `<div class="result_chunk">${btns_text_random_chunk[i]}</div>`;
    }
});

